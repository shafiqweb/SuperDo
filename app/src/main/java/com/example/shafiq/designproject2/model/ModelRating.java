package com.example.shafiq.designproject2.model;

public class ModelRating {
    private int ownerImageList;
    private String ownerName;
    private String ownerAddress;
    private int ownerAvailableImage;
    private String ownerAvailableText;

    public int getOwnerImageList() {
        return ownerImageList;
    }

    public void setOwnerImageList(int ownerImageList) {
        this.ownerImageList = ownerImageList;
    }

    public String getOwnerName() {
        return ownerName;
    }

    public void setOwnerName(String ownerName) {
        this.ownerName = ownerName;
    }

    public String getOwnerAddress() {
        return ownerAddress;
    }

    public void setOwnerAddress(String ownerAddress) {
        this.ownerAddress = ownerAddress;
    }

    public int getOwnerAvailableImage() {
        return ownerAvailableImage;
    }

    public void setOwnerAvailableImage(int ownerAvailableImage) {
        this.ownerAvailableImage = ownerAvailableImage;
    }

    public String getOwnerAvailableText() {
        return ownerAvailableText;
    }

    public void setOwnerAvailableText(String ownerAvailableText) {
        this.ownerAvailableText = ownerAvailableText;
    }

}
